# Simple Python Program: Greet user and calculate age next year

# Get user input for name and age
name = input("What is your name? ")
age = int(input("How old are you? "))

# Calculate age next year
next_year_age = age + 1

# Print a greeting message with the user's name and age next year
print(f"Hello, {name}! You are {age} years old now.")
print(f"Next year, you'll be {next_year_age} years old.")

